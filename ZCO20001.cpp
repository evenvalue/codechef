void solve() {
  int64 p, idx;
  cin >> p >> idx;
  int64 ans = 0;
  for (int64 mid = (1LL << (p - 1)), i = 0; idx != 0; mid /= 2, i++) {
    if (idx >= mid) {
      ans += (1LL << i);
      idx -= mid;
    }
  }
  cout << ans << '\n';
}

